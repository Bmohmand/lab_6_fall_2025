import karel


def main():
    pupper = karel.KarelPupper()
    pupper.wiggle()
    pupper.bob()
    pupper.dance()
    pupper.bark()
    pupper.stop()
    pupper.move_forward()
    pupper.move_backward()
    pupper.move_left()
    pupper.move_right()
    pupper.turn_left()
    pupper.turn_right()
    
    

if __name__ == '__main__':
    main()
